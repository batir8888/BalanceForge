namespace BalanceForge.Services
{
    /// <summary>
    /// Интерфейс для валидаторов значений ячеек таблицы баланса.
    /// Позволяет определять кастомные правила проверки для значений столбцов.
    /// Реализующие классы должны проверять соответствие значения определенным критериям.
    /// </summary>
    public interface IValidator
    {
        /// <summary>
        /// Проверяет соответствует ли значение правилам валидации.
        /// </summary>
        /// <param name="value">Значение для проверки.</param>
        /// <returns>true если значение прошло валидацию, иначе false.</returns>
        bool Validate(object value);
        
        /// <summary>
        /// Получает сообщение об ошибке валидации.
        /// Возвращается пользователю если значение не прошло проверку Validate.
        /// </summary>
        /// <returns>Строка с описанием причины ошибки валидации.</returns>
        string GetErrorMessage();
    }
}